    private java.sql.Date obtenerFechaEnSQL(JXDatePicker fecha) {
        //meses empiezan en 0
        Calendar cal = Calendar.getInstance();
        cal.setTime(fecha.getDate());
        int year = cal.get(Calendar.YEAR) - 1900; //PORQUE PUTAS
        int month = cal.get(Calendar.MONTH);
        int day = cal.get(Calendar.DAY_OF_MONTH);
        java.sql.Date sqlDate;

        sqlDate = new java.sql.Date(year, month, day);
        return sqlDate;
    }
