    private void btnIngresarPersonaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarPersonaActionPerformed
        BigDecimal cedula = null;
        boolean todoBien = true;
        try {
            cedula = new BigDecimal(txtCedulaInsert.getText());

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Tiene que ingresar solo números", "Error", JOptionPane.ERROR_MESSAGE);
            todoBien = false;
        }
        if (todoBien == true) {

            Persona miPersona = new Persona();
            String psNombre = txtNombreInsert.getText();
            String[] tel = null;
            String telefonos = txtTelefonoInsert.getText() + "/";
            if (txtTelefonoInsert.getText() != "") {
                tel = txtTelefonoInsert.getText().split("/");
            }

            Vector<String> nombre = new Vector();
            Vector<Integer> listaTelefonos = new Vector();
            if (tel != null) {
                for (int i = 0; i < tel.length; ++i) {
                    listaTelefonos.add(Integer.parseInt(tel[i]));
                }
            }

            nombre.add(psNombre);
            String apellido = txtPrimerApellido.getText();
            if (apellido == "") {
                apellido = "  ";
            }

            nombre.add(apellido);
            nombre.add(txtSegApellido.getText());
            if (nombre.size() < 3) {
                if (nombre.size() < 2) {
                    nombre.add(" ");
                    nombre.add(" ");

                } else {
                    nombre.add(" ");

                }

            }

            miPersona.setNombre(nombre.elementAt(0));
            miPersona.setApellido1(nombre.elementAt(1));
            miPersona.setApellido2(nombre.elementAt(2));

            miPersona.setProvincia(txtProvIns.getText());
            miPersona.setDistrito(txtDisIns.getText());
            miPersona.setCanton(txtCantonIns.getText());
            miPersona.setCedula(cedula);
            miPersona.setEC(txtECInsert.getText());
            miPersona.setExpLab(txtAExpLabInsert.getText());
            if (InsertFecha.getDate() != null && InsertFechaNac.getDate() != null) {
                miPersona.setFechaAsp(obtenerFechaEnSQL(InsertFecha));
                miPersona.setFechaNac(obtenerFechaEnSQL(InsertFechaNac));
            }

            if (txtSexoInsert.getText() != "") {
                miPersona.setSexo(txtSexoInsert.getText().charAt(0));

            } else {
                miPersona.setSexo('O');
            }
            miPersona.setNumDep(cmbBoxDepCand.getSelectedIndex());
            miPersona.setNumHijos(Integer.parseInt(txtNumHijosInsert.getText()));
            miPersona.setObs(txtAObsInsert.getText());
            miPersona.setSueldoAspirado(new BigDecimal(txtSueldoAspInsert.getText()));
            miPersona.setTipoEmpresa(cmbBoxEmpresa.getSelectedIndex());
            miPersona.setGrupoOrden((txtGrupoOrdenInsert.getText()).charAt(0));
            miPersona.setCargo(txtCargoInsertar.getText());
            boolean ok = false;
            if (InsertFecha != null && InsertFechaNac != null) {
                ok = misConsultas.insertarPersona(miPersona);
            }
            if (ok) {
                if (tel != null) {
                    misConsultas.insertarTelefonos(miPersona.getCedula(), listaTelefonos);

                }
                panelModificarGeneral.removeAll();
                panelModificarGeneral.repaint();
                panelModificarGeneral.revalidate();
                panelModificarGeneral.add(panelPadreEstudios);
                panelModificarGeneral.repaint();
                panelModificarGeneral.revalidate();
            }

        }
    }//GEN-LAST:event_btnIngresarPersonaActionPerformed

