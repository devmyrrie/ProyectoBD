    private void auxNombreCompleto() {

        Persona laPersona = new Persona();
        Departamento departamentoAplica = new Departamento();
        Vector<Integer> telefonos = new Vector();
        boolean ok = true;
        if (txtApellido1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el primer apellido");
            ok = false;
        }
        if (ok == true) {
            laPersona = misConsultas.consultaInfo(txtNombre.getText(), txtApellido1.getText(), txtApellido2.getText());
            if (laPersona != null) {
                departamentoAplica = misConsultas.consultarDepartamento(laPersona.getCedula());
                telefonos = misConsultas.consultarTelefonos(laPersona.getCedula());
                setTelefono(telefonos);
                setPersona(laPersona, 0);
                setDepartamento(departamentoAplica);
                Vector<Estudios> est = new Vector();
                est = misConsultas.consultarEstudios(laPersona.getCedula());

                setEstudios(est, 0);
                txtADir.setEditable(true);

                JListNombres.setListData(misConsultas.listaNombresIguales(laPersona.getNombre(), laPersona.getApellido1(), laPersona.getApellido2(), laPersona.getCedula()));
                if (JListNombres.getModel().getSize() != 0) {
                    jPanel6.setVisible(true);
                } else {
                    jPanel6.setVisible(false);
                }
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró a la persona con ese nombre en la base de datos", "Error", JOptionPane.ERROR_MESSAGE);
                limpiarCampos();

            }
        }

    }
