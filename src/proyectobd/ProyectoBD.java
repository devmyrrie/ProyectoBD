    private void setTelefono(Vector<Integer> telefono) {
        String telef = "";
        Iterator x = telefono.iterator();
        while (x.hasNext()) {
            telef += x.next();
            if (x.hasNext()) {
                telef += " - ";
            }
        }
        txtTelefono.setText(telef);

    }

    private void setEstudios(Vector<Estudios> est, int opc) {
        Estudios misEstudios = new Estudios();
        Iterator x = est.iterator();
        String miString = "";
        while (x.hasNext()) {
            misEstudios = (Estudios) x.next();
            miString += "Estudió en: \n ";

            switch (misEstudios.getTipoEstudio()) {
                case 0:
                    miString += "*Primaria en: ";
                    break;
                case 1:
                    miString += "*Secundaria en: ";
                    break;
                default:
                    break;

            }
            miString = miString + (misEstudios.getNombreInst()) + ", estudios ";
            int comp = misEstudios.getFinalizacion();
            if (comp == 0) {
                miString += " incompletos. ";
            } else {
                miString += " completos. ";
            }

            if (misEstudios.getTipoEstudio() > 1 && misEstudios.getTipoEstudio() != 4) {
                miString = miString + (misEstudios.getGradoAcademico()) + " en " + misEstudios.getNombreCarrera();
            }

            miString += " \n";
        }
        if (opc == 0) {
            txtAEstudios.setText(miString);

        } else if (opc == 1) {

            txtAEstudiosRango.setText(miString);
        } else if (opc == 2) {
            EstudiosFechaGrupo.setText(miString);
        }

    }

