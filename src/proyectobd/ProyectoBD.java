    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        boolean todoBien = true;
        boolean ok = true;

        BigDecimal cedula = null;
        try {
            cedula = new BigDecimal(txtCedulaMod.getText());

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Tiene que ingresar solo números", "Error", JOptionPane.ERROR_MESSAGE);
            todoBien = false;
        }
        if (todoBien == true) {
            switch (cmbCobxModDato.getSelectedIndex()) {

                case 0:
                    todoBien = misConsultas.actualizarNombre(txtDatoMod.getText(), cedula);

                    break;

                case 1:
                    todoBien = misConsultas.actualizarApellido(txtDatoMod.getText(), cedula);

                    break;

                case 2:
                    todoBien = misConsultas.actualizarSegundoApellido(txtDatoMod.getText(), cedula);

                    break;

                case 3:
                    todoBien = misConsultas.actualizarSexo(String.valueOf(txtDatoMod.getText().charAt(0)), cedula);
                    break;

                case 4:
                    todoBien = misConsultas.actualizarProvincia(txtDatoMod.getText(), cedula);

                    break;

                case 5:
                    todoBien = misConsultas.actualizarCanton(txtDatoMod.getText(), cedula);

                    break;

                case 6:
                    todoBien = misConsultas.actualizarDistrito(txtDatoMod.getText(), cedula);

                    break;

                case 7:
                    Integer sueldo = null;
                    ok = true;
                    try {
                        sueldo = new Integer(txtDatoMod.getText());

                    } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(null, "Tiene que ingresar solo números", "Error", JOptionPane.ERROR_MESSAGE);
                        ok = false;
                    }
                    if (ok) {
                        todoBien = misConsultas.actualizarSueldo(new BigDecimal(txtDatoMod.getText()), cedula);

                    }
                    break;

                case 8:
                    todoBien = misConsultas.actualizarEC(txtDatoMod.getText(), cedula);

                    break;

                case 9:
                    Integer num = null;
                    ok = true;
                    try {
                        num = new Integer(txtDatoMod.getText());

                    } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(null, "Tiene que ingresar solo números", "Error", JOptionPane.ERROR_MESSAGE);
                        ok = false;
                    } finally {
                        if (ok) {
                            todoBien = misConsultas.actualizarNumHijos(Integer.parseInt(txtDatoMod.getText()), cedula);

                        }
                    }
                    break;

                case 10:
                    todoBien = misConsultas.actualizarCargo(txtDatoMod.getText(), cedula);
                    break;

                case 11:
                    int op = cmbBoxEmpresaMod.getSelectedIndex();
                    todoBien = misConsultas.actualizarEmpresa(op, cedula);
                    break;

                case 12:
                    int op2 = cmbBoxEmpresaMod.getSelectedIndex();
                    todoBien = misConsultas.actualizarDepartamento(op2, cedula);
                    break;

            }

        }
        if (todoBien == true) {
            JOptionPane.showMessageDialog(null, "Se realizó la modificación");

        } else {
            if (ok == true) {
                JOptionPane.showMessageDialog(null, "No se realizó la modificación");

            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

